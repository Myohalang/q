using System;
using static System.Console;

namespace AddLib
{
    public class AddClass
    {
        public double double_add(double a, double b)
        {
            return a + b;
        }

        public int int_add(int a, int b)
        {
            return a * b; // 테스트를 위해 일부러 오타 작성
        }
    }
}
